
public class OperationsPractice {

	public static void main(String[] args) 
	{
		int number; //Data type then variable to declare variable
		
		number = 20;
		System.out.println("initial number: " + number);
		
		number +=5; //number = number + 5
		System.out.println("add 5: " + number);
		
		number /=2; //number = number / 2
		System.out.println("divide by 2: " + number);
		
		number *=4; //number = number * 4
		System.out.println("multiply by 4: " + number);
		
		number -=6; //number = number - 6
		System.out.println("subtract 6: " + number);
		
		number %=2; //number = number / 2
		System.out.println("modularly divide by 2: " + number);
		
		
	}

}
